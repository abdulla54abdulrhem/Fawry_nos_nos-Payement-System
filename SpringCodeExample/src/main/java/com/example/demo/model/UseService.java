package com.example.demo.model;

public class UseService {
    private int id;
    private int serviceTypeNumber;
    private int mainServicesNumber;

    public void setId(int id) {
        this.id = id;
    }

    public void setServiceTypeNumber(int serviceTypeNumber) {
        this.serviceTypeNumber = serviceTypeNumber;
    }

    public void setMainServicesNumber(int mainServicesNumber) {
        this.mainServicesNumber = mainServicesNumber;
    }

    public int getId() {
        return id;
    }

    public int getServiceTypeNumber() {
        return serviceTypeNumber;
    }

    public int getMainServicesNumber() {
        return mainServicesNumber;
    }
}
